package com;

public class decrementExample {
    public static void main(String[] args) {
        // Consider a number 22 pre decrement it six times and list all the values when decrement each

        //Decrement
        // Pre-decrement-> --x

        int num1;
        num1=22;

        System.out.println("\n");
        System.out.println("Initial Value is "+num1);

        //1st time pre decrement
        System.out.println("\n");
        System.out.println("1st time pre decrement  : "+--num1);

        //2nd time pre decrement
        System.out.println("2nd time pre decrement  : "+--num1);

        //3rd time pre decrement
        System.out.println("3rd time pre decrement  : "+--num1);

        //4th time pre decrement
        System.out.println("4th time pre decrement  : "+--num1);

        //5th time pre decrement
        System.out.println("5th time pre decrement  : "+--num1);

        //6th time pre decrement
        System.out.println("6th time pre decrement  : "+--num1);


    }

}
